import javax.swing.*;;
public class Potência {
   
            public static void main(String[] args) {
     
            
                
                int numero = Integer.parseInt(JOptionPane.showInputDialog("Informe um Numero: "));

                for (int i = 1; i <= numero; i++) {
                    int quadrado = i * i;
                    System.out.println(i + "² = " + quadrado);
                }
                
            }
        }
        

   
    
    

